"""
Proof-of-Concept generator.

Every confirmed finding must have a reproducible PoC before it can be
included in a report ("No exploit, no report" principle).  PocGenerator
uses Claude HAIKU to produce a curl command or a Python requests script
from the finding metadata captured during validation.

If Claude is unavailable a deterministic template-based fallback is used
so a PoC is always generated — even if it is less polished.
"""
from __future__ import annotations

import json
import logging
from typing import Any, Dict

logger = logging.getLogger(__name__)

_HAIKU_MODEL = "claude-haiku-4-5"

# ---------------------------------------------------------------------------
# Template-based fallback PoC generation
# ---------------------------------------------------------------------------

_CURL_TEMPLATE = """\
#!/usr/bin/env bash
# PoC for {name} ({type})
# Target: {url}
# Parameter: {parameter}
# Severity: {severity}
# Generated by Overwatch

curl -sk -X {method} \\
  '{url}' \\
  {headers_block}\\
  {body_block}--data-urlencode '{parameter}={payload}'
"""

_PYTHON_TEMPLATE = """\
#!/usr/bin/env python3
\"\"\"
Proof-of-Concept for {name} ({type})
Target:    {url}
Parameter: {parameter}
Severity:  {severity}

Generated by Overwatch — for authorised testing only.
\"\"\"
import requests

TARGET = "{url}"
PARAMETER = "{parameter}"
PAYLOAD = {payload_repr}

session = requests.Session()
session.verify = False  # Ignore SSL for pentest targets

response = session.{method_lower}(
    TARGET,
    {request_kwargs},
)

print(f"Status: {{response.status_code}}")
print(f"Response (first 500 chars):")
print(response.text[:500])

# Expected indicator of success:
# {expected_indicator}
"""


def _build_curl_headers(headers: Dict[str, str]) -> str:
    if not headers:
        return ""
    lines = [f"  -H '{k}: {v}' \\" for k, v in headers.items()]
    return "\n".join(lines) + "\n"


def _build_python_kwargs(method: str, parameter: str, payload: str) -> str:
    method_lower = method.lower()
    if method_lower == "get":
        return f"params={{PARAMETER: PAYLOAD}}"
    elif method_lower == "post":
        return f"data={{PARAMETER: PAYLOAD}}"
    return f"params={{PARAMETER: PAYLOAD}}"


class PocGenerator:
    """
    Generates reproducible proof-of-concept exploits in multiple formats.

    Supported formats:
        curl   – A self-contained bash curl invocation
        python – A Python 3 requests script
    """

    def __init__(self, claude_client=None) -> None:
        self._claude = claude_client

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    async def generate_poc(self, finding: Dict[str, Any], format: str = "curl") -> str:
        """
        Generate a PoC for *finding* in the requested *format*.

        Args:
            finding: Dict with at minimum: url, type, parameter, payload.
                     Optional: method, headers, name, severity, description,
                     expected_response.
            format:  "curl" or "python".

        Returns:
            String containing the PoC code.
        """
        format = format.lower()
        if format == "curl":
            return await self.generate_curl(finding)
        elif format == "python":
            return await self.generate_python_script(finding)
        else:
            logger.warning("Unknown PoC format %r — defaulting to curl", format)
            return await self.generate_curl(finding)

    async def generate_curl(self, finding: Dict[str, Any]) -> str:
        """
        Generate a curl PoC command for *finding*.

        Tries Claude HAIKU first; falls back to a deterministic template.
        """
        if self._claude is not None:
            result = await self._ask_claude(finding, "curl")
            if result:
                return result

        return self._template_curl(finding)

    async def generate_python_script(self, finding: Dict[str, Any]) -> str:
        """
        Generate a Python requests PoC script for *finding*.

        Tries Claude HAIKU first; falls back to a deterministic template.
        """
        if self._claude is not None:
            result = await self._ask_claude(finding, "python")
            if result:
                return result

        return self._template_python(finding)

    # ------------------------------------------------------------------
    # Claude invocation
    # ------------------------------------------------------------------

    async def _ask_claude(self, finding: Dict[str, Any], fmt: str) -> str:
        """
        Ask Claude to generate a PoC.

        Returns the generated string, or empty string on failure.
        """
        finding_json = json.dumps(finding, indent=2, default=str)

        if fmt == "curl":
            format_instruction = (
                "Generate a single self-contained bash curl command that reproduces "
                "this finding.  Add a brief comment block at the top explaining what "
                "it tests.  Output ONLY the bash script, no explanation outside the script."
            )
        else:
            format_instruction = (
                "Generate a complete, runnable Python 3 script using the requests library "
                "that reproduces this finding.  Include a docstring explaining the PoC.  "
                "Output ONLY the Python script, no explanation outside the script."
            )

        prompt = (
            f"You are a penetration testing PoC generator.\n\n"
            f"FINDING:\n{finding_json}\n\n"
            f"{format_instruction}\n\n"
            f"This is for authorised penetration testing only."
        )

        try:
            response = await self._claude.messages.create(
                model=_HAIKU_MODEL,
                max_tokens=1024,
                messages=[{"role": "user", "content": prompt}],
            )
            content = response.content[0].text

            # Strip markdown code fences if present
            if "```bash" in content:
                content = content.split("```bash")[1].split("```")[0].strip()
            elif "```python" in content:
                content = content.split("```python")[1].split("```")[0].strip()
            elif "```" in content:
                content = content.split("```")[1].split("```")[0].strip()

            return content.strip()

        except Exception as exc:
            logger.warning("Claude PoC generation failed: %s", exc)
            return ""

    # ------------------------------------------------------------------
    # Template-based fallbacks
    # ------------------------------------------------------------------

    def _template_curl(self, finding: Dict[str, Any]) -> str:
        """Build a deterministic curl PoC from finding fields."""
        url = finding.get("url", "https://target.example.com/")
        method = (finding.get("method") or "GET").upper()
        parameter = finding.get("parameter", "q")
        payload = finding.get("payload", "' OR 1=1 --")
        headers = finding.get("headers") or {}
        name = finding.get("name") or finding.get("type") or "vulnerability"
        vuln_type = finding.get("type", "unknown")
        severity = finding.get("severity", "unknown")

        headers_block = _build_curl_headers(headers)
        body_block = ""
        if method == "POST":
            body_block = "-X POST \\\n  "

        return _CURL_TEMPLATE.format(
            name=name,
            type=vuln_type,
            url=url,
            parameter=parameter,
            payload=payload,
            method=method,
            severity=severity,
            headers_block=headers_block,
            body_block=body_block,
        )

    def _template_python(self, finding: Dict[str, Any]) -> str:
        """Build a deterministic Python PoC from finding fields."""
        url = finding.get("url", "https://target.example.com/")
        method = (finding.get("method") or "GET").upper()
        parameter = finding.get("parameter", "q")
        payload = finding.get("payload", "' OR 1=1 --")
        name = finding.get("name") or finding.get("type") or "vulnerability"
        vuln_type = finding.get("type", "unknown")
        severity = finding.get("severity", "unknown")
        expected = finding.get("expected_indicator") or "HTTP 200 with error string in body"

        request_kwargs = _build_python_kwargs(method, parameter, payload)

        return _PYTHON_TEMPLATE.format(
            name=name,
            type=vuln_type,
            url=url,
            parameter=parameter,
            payload=payload,
            payload_repr=repr(payload),
            method_lower=method.lower(),
            severity=severity,
            request_kwargs=request_kwargs,
            expected_indicator=expected,
        )
